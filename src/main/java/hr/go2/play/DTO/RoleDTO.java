package hr.go2.play.DTO;

public class RoleDTO {

	private Long id;

	private String name;


	public RoleDTO() {
	}
	

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
